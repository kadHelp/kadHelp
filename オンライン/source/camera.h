class cCamera
{
private:
	iexView*	view;
	
	//------------------------------------------------------
	//	基本パラメータ
	//------------------------------------------------------
	int		mode;	//	モード
	Vector3	pos;	//	位置
	Vector3	target;	//	注視点

	//------------------------------------------------------
	//		基本行動
	//------------------------------------------------------
	void	Normal();	//	通常カメラ

	//------------------------------------------------------
	//	その他パラメータ
	//------------------------------------------------------
	float	max_dist;	//	最大距離
	float	min_dist;	//	最小距離

	Vector3	PlayerPos;	//	プレイヤーの位置

public:
	//------------------------------------------------------
	//	初期化・解放
	//------------------------------------------------------
	cCamera( );
	~cCamera( );

	//------------------------------------------------------
	//	更新
	//------------------------------------------------------
	void Update();
	void Activate();

	Vector3 getPos(){ return pos; }

	//------------------------------------------------------
	//	セッター
	//------------------------------------------------------
	void SetMode( int mode ){ this->mode = mode; }
	void SetPlayerPos( Vector3& pos ){ PlayerPos = pos; }
	void SetPos( Vector3& pos ){ this->pos = pos; }
	void SetTarget( Vector3& target ){ this->target = target; }
	

};

#define	CAMERA_NORMAL	0
#define	CAMERA_FIXED	1